function test_sound_absorption_sea_ainslie()

    test(1) = struct('T', 8, 'S', 35, 'D', 50, 'f', 1, 'pH', 8, ...
        'Boric', 0.0529071656204235, ...
        'MgSO4', 0.009094246694179969, ...
        'H2O',   0.0003632778317872613, ...
        'alpha', 0.06236469014639073);
    
    test(2) = struct('T', 28, 'S', 33, 'D', 250, 'f', 21, 'pH', 7, ...
        'Boric', 0.03908031100850023, ...
        'MgSO4', 1.5560181958646795, ...
        'H2O',   0.07548636683132491, ...
        'alpha', 1.6705848737045046);

    ok = false(numel(test), 4);
    for i = 1:numel(test)
        [alpha, Boric, MgSO4, H2O] = ...
            sound_absorption_sea_ainslie( ...
            test(i).T, test(i).S, test(i).D, test(i).f, test(i).pH);
        
        ok(i,:) = [abs(Boric - test(i).Boric) < eps ...
                   abs(MgSO4 - test(i).MgSO4) < eps ...
                   abs(H2O - test(i).H2O) < eps ...
                   abs(alpha - test(i).alpha) < eps];
    end

    stack = dbstack;
    if all(ok)
        disp([stack.name ' PASSED']);
    else
        warning([stack.name ' FAILED'])
    end
end

