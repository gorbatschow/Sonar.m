function test_sound_absorption_sea_francois()

    test(1) = struct('T', 8, 'S', 35, 'D', 50, 'f', 1, 'pH', 8, ...
        'Boric', 0.051973051193935221, ...
        'MgSO4', 0.008927280945054484, ...
        'H2O',   0.00033647882051690006, ...
        'alpha', 0.0612368109595066);
    
    test(2) = struct('T', 28, 'S', 33, 'D', 250, 'f', 21, 'pH', 7, ...
        'Boric', 0.03256206471089461, ...
        'MgSO4', 1.6346417355314038, ...
        'H2O',   0.07640836164334801, ...
        'alpha', 1.7436121618856464);

    ok = false(numel(test), 4);
    for i = 1:numel(test)
        [alpha, Boric, MgSO4, H2O] = ...
            sound_absorption_sea_francois( ...
            test(i).T, test(i).S, test(i).D, test(i).f, test(i).pH);
        
        ok(i,:) = [abs(Boric - test(i).Boric) < eps ...
                   abs(MgSO4 - test(i).MgSO4) < eps ...
                   abs(H2O - test(i).H2O) < eps ...
                   abs(alpha - test(i).alpha) < eps];
    end

    stack = dbstack;
    if all(ok)
        disp([stack.name ' PASSED']);
    else
        warning([stack.name ' FAILED'])
    end
end

